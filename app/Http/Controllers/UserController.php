            ->where('transactions.user_id','=',$id)
            ->pluck('transactions.item_id')
            ->toArray();
        $transactions = DB::table('transactions')
            ->select('transactions.*')
            ->where('transactions.user_id','=',$id)
            ->get();
//        dd($transactions);
        $items = DB::table('items')
            ->whereIn('items.id',$item_ids)
            ->get();
        return view('layouts.profile',compact('user', 'items','transactions'));
